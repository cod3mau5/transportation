<?php

/* Default Font Family */
function daisy_blog_get_default_site_identity_font_family() {    
    return esc_html__( "Oswald", 'daisy-blog' );
}

function daisy_blog_get_default_main_font_family() {    
    return esc_html__( "Oswald", 'daisy-blog' );
}

function daisy_blog_get_default_secondary_font_family() {    
    return esc_html__( "Roboto", 'daisy-blog' );
}